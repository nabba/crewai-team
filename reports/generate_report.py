    story.append(Paragraph(
        "<b>Recommendation:</b> Establish 3-tier policy: safety-critical paths use logger.error() + alert, "
        "operational paths use logger.warning(), best-effort paths (telemetry) use logger.debug(). "
        "Ban silent pass entirely.",
        styles["Body"]))

    story.append(Paragraph("1.2 File I/O Without Atomic Writes", styles["H2"]))
