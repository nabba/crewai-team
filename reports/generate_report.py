from reportlab.lib.styles import getSampleStyleSheet, ParagraphStyle
from reportlab.lib.units import mm, cm
from reportlab.lib.colors import HexColor, black, white
from reportlab.lib.enums import TA_CENTER, TA_LEFT, TA_JUSTIFY
from reportlab.platypus import (
    SimpleDocTemplate, Paragraph, Spacer, Table, TableStyle,
    PageBreak, HRFlowable,
)

OUTPUT = "/Users/andrus/BotArmy/crewai-team/reports/andrusai_system_analysis.pdf"

# Colors
DARK = HexColor("#1a1a2e")
ACCENT = HexColor("#e94560")
BLUE = HexColor("#0f3460")
LIGHT_GRAY = HexColor("#f5f5f5")
MED_GRAY = HexColor("#e0e0e0")
WHITE = HexColor("#ffffff")

styles = getSampleStyleSheet()

# Custom styles
styles.add(ParagraphStyle("ReportTitle", parent=styles["Title"], fontSize=28,
                          textColor=DARK, spaceAfter=6, alignment=TA_CENTER))
styles.add(ParagraphStyle("Subtitle", parent=styles["Normal"], fontSize=14,
                          textColor=BLUE, alignment=TA_CENTER, spaceAfter=20))
styles.add(ParagraphStyle("H1", parent=styles["Heading1"], fontSize=18,
                          textColor=DARK, spaceBefore=20, spaceAfter=10,
                          borderWidth=1, borderColor=ACCENT, borderPadding=4))
styles.add(ParagraphStyle("H2", parent=styles["Heading2"], fontSize=14,
                          textColor=BLUE, spaceBefore=14, spaceAfter=6))
styles.add(ParagraphStyle("H3", parent=styles["Heading3"], fontSize=11,
                          textColor=DARK, spaceBefore=10, spaceAfter=4))
styles.add(ParagraphStyle("Body", parent=styles["Normal"], fontSize=9.5,
                          leading=13, alignment=TA_JUSTIFY, spaceAfter=6))
styles.add(ParagraphStyle("BodyBold", parent=styles["Normal"], fontSize=9.5,
                          leading=13, alignment=TA_LEFT, spaceAfter=4,
                          fontName="Helvetica-Bold"))
styles.add(ParagraphStyle("Score", parent=styles["Normal"], fontSize=36,
                          textColor=ACCENT, alignment=TA_CENTER,
                          fontName="Helvetica-Bold", spaceAfter=4))
styles.add(ParagraphStyle("ScoreLabel", parent=styles["Normal"], fontSize=10,
                          textColor=BLUE, alignment=TA_CENTER, spaceAfter=16))
styles.add(ParagraphStyle("BulletItem", parent=styles["Normal"], fontSize=9.5,
                          leading=13, leftIndent=20, bulletIndent=10,
                          spaceAfter=3))
styles.add(ParagraphStyle("SmallNote", parent=styles["Normal"], fontSize=8,
                          textColor=HexColor("#666666"), alignment=TA_CENTER))


def make_table(headers, rows, col_widths=None):
    data = [headers] + rows
    t = Table(data, colWidths=col_widths, repeatRows=1)
    t.setStyle(TableStyle([
        ("BACKGROUND", (0, 0), (-1, 0), BLUE),
        ("TEXTCOLOR", (0, 0), (-1, 0), WHITE),
        ("FONTNAME", (0, 0), (-1, 0), "Helvetica-Bold"),
        ("FONTSIZE", (0, 0), (-1, 0), 8.5),
        ("FONTSIZE", (0, 1), (-1, -1), 8.5),
        ("ALIGN", (0, 0), (-1, 0), "CENTER"),
        ("VALIGN", (0, 0), (-1, -1), "MIDDLE"),
        ("ROWBACKGROUNDS", (0, 1), (-1, -1), [WHITE, LIGHT_GRAY]),
        ("GRID", (0, 0), (-1, -1), 0.5, MED_GRAY),
        ("TOPPADDING", (0, 0), (-1, -1), 4),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 4),
        ("LEFTPADDING", (0, 0), (-1, -1), 6),
        ("RIGHTPADDING", (0, 0), (-1, -1), 6),
    ]))
    return t


